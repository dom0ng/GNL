#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		i = i + 1;
	}
	if (!c && s[i] == '\0')
		return ((char *)s + i);
	return (NULL);
}
