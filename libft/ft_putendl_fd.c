#include "libft.h"

void	ft_putendl_fd(const char *s, int fd)
{
	char	c;

	c = '\n';
	if (s != NULL)
	{
		while (*s)
		{
			write(fd, s++, 1);
		}
		write(fd, &c, 1);
	}
}
