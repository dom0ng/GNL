#include "libft.h"

size_t	ft_memicmp(const char *s1, const char *s2, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		if (s1[i] != s2[i])
			return (i);
		i++;
	}
	return (len);
}
